package fs.four.human.Login.VO;

public class SignupVO {
        private String uId;
        private String uPwd;
        private String uEmail;
        private String uNickname;
        private String uCc;
        private String uPhotoName;
        private String uPhotoPath;
        private String uPhone;

        public String getuId() {
                return uId;
        }

        public void setuId(String uId) {
                this.uId = uId;
        }

        public String getuPwd() {
                return uPwd;
        }

        public void setuPwd(String uPwd) {
                this.uPwd = uPwd;
        }

        public String getuEmail() {
                return uEmail;
        }

        public void setuEmail(String uEmail) {
                this.uEmail = uEmail;
        }

        public String getuNickname() {
                return uNickname;
        }

        public void setuNickname(String uNickname) {
                this.uNickname = uNickname;
        }

        public String getuCc() {
                return uCc;
        }

        public void setuCc(String uCc) {
                this.uCc = uCc;
        }

        public String getuPhotoName() {
                return uPhotoName;
        }

        public void setuPhotoName(String uPhotoName) {
                this.uPhotoName = uPhotoName;
        }

        public String getuPhotoPath() {
                return uPhotoPath;
        }

        public void setuPhotoPath(String uPhotoPath) {
                this.uPhotoPath = uPhotoPath;
        }

        public String getuPhone() {
                return uPhone;
        }

        public void setuPhone(String uPhone) {
                this.uPhone = uPhone;
        }
}
